// Esto es un programa "Hello, world!" en Rust. Las funciones se crean con el keyword 'fn'
// y 'main' es lo primero en ejecutarse en cualquier programa escrito en Rust.
fn main() {
  // 'println!' es una "macro", si fuera una función, se llamaría sin el ! (signo
  // de excalamación). De esto se hablará más adelante.
  println!("Hello, World!");
  // Rust usa el ; (punto y coma) para finalizar las líneas de intrucciones (en su mayoría)
}